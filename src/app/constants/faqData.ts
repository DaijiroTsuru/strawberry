/**
 * 指定されたタグに基づいてFAQをフィルタリングする
 * AND条件でフィルターする
 */
export function filterFaqsByTags(tags: FaqTag[]): FaqCategory[] {
  if (tags.length === 0) return faqCategories;

  return faqCategories
    .map((category) => ({
      ...category,
      questions: category.questions.filter((q) =>
        tags.some((tag) => q.tags.includes(tag))
      ),
    }))
    .filter((category) => category.questions.length > 0);
}
